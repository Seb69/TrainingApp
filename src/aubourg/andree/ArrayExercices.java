package aubourg.andree;

public class ArrayExercices {
	
	private String exeNumber ;
	private String exeTitle;
	private String exeDescription; 
	private String exeTime;
	
	public String getExeNumber() {
		return exeNumber;
	}
	public void setExeNumber(String exeNumber) {
		this.exeNumber = exeNumber;
	}
	public String getExeTitle() {
		return exeTitle;
	}
	public void setExeTitle(String exeTitle) {
		this.exeTitle = exeTitle;
	}
	public String getExeDescription() {
		return exeDescription;
	}
	public void setExeDescription(String exeDescription) {
		this.exeDescription = exeDescription;
	}
	public String getExeTime() {
		return exeTime;
	}
	public void setExeTime(String exeTime) {
		this.exeTime = exeTime;
	}

}
